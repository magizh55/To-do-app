## 💡 Project Description

Nexus OS is a **smart task reminder application** where users can:

- create tasks
- set deadlines
- assign time
- toggle reminders
- delete tasks

All tasks are stored in the browser using **localStorage**, so the data remains even after refreshing the page.

---

## ⚙ Core Functionalities

### 🔹 Add Task
Users can add tasks with:
- emoji category
- task title
- date
- time

---

### 🔹 Reminder Toggle
Users can enable or disable task reminder status using:

```text
🔔 Active
🔕 Inactive
```

---

### 🔹 Task Counter
Shows the number of active task nodes:

```text
3 Nodes
5 Nodes
```

---

### 🔹 Persistent Storage
Tasks are saved in:

```javascript
localStorage
```

This ensures no task data is lost after refresh.

