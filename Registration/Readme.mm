# 🔐 Modern Login and Registration System

A modern and responsive **Login & Registration System** built using **HTML, CSS, and JavaScript**.

This project features a clean **glassmorphism UI design**, animated gradient background, user authentication using **localStorage**, and a separate **dashboard page**.

Perfect for:
- 🎓 College mini projects
- 💼 Portfolio / GitHub showcase
- 🌐 Frontend authentication demo
- 📚 Learning HTML, CSS, JS

---

## ✨ Features

- 🔑 User Login
- 📝 New User Registration
- 👁 Show / Hide Password
- 💾 Remember Me Option
- 🔒 Password Strength Indicator
- 🎨 Modern Glassmorphism UI
- 🌈 Animated Gradient Background
- 👤 Welcome Dashboard
- 🚪 Logout Functionality
- 💽 Data stored using localStorage
- 📱 Responsive Design
- ⚠ Duplicate Email Validation
- ✅ Password Confirmation Check

