## 💡 Project Description

This project simulates a frontend authentication system where user records are stored in the browser using **localStorage**.

It includes:

- Login validation
- Registration validation
- Session management
- Dashboard redirection

This project does not require any backend or database server, making it easy to host on **GitHub Pages**.

